#include<iostream>
using namespace std;
 
void solve()
{
    int n;
    cin>>n;
    for(int i=-1*(n-1);i<n;i++)
    {
        for(int j=-1*(n-1);j<n;j++)
        {
            cout<<max(abs(i),abs(j))+1<<" ";
        }
        cout<<endl;
    }
}
 
